-- Purpose: Grant ATTR_MGR schema access to MERCH/OMNI/VSTORE objects needed
--          for Shopify Hub and Live Publication Testing.
--
-- Two options provided:
--   Option A: GRANTS + SYNONYMS (same database - RECOMMENDED for demo)
--   Option B: DB LINKS (if schemas are on different databases)
--
-- Pattern: PAT-ORACLE-PRODUCTION-GRADE-SQL-01
-- Run as: SYS or DBA with GRANT privileges
