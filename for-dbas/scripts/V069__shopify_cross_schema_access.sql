SET DEFINE OFF;
SET SERVEROUTPUT ON;

PROMPT ============================================================
PROMPT V069: Shopify Cross-Schema Access Setup
PROMPT ============================================================

-- ============================================================================
-- OPTION A: GRANTS + SYNONYMS (Same Database)
-- ============================================================================
-- This is the preferred approach for demo environments where all schemas
-- are in the same Oracle instance (e.g., DEMODB).

PROMPT
PROMPT --- STEP 1: GRANTS FROM MERCH TO ATTR_MGR ---

-- Read access to product master data
GRANT SELECT ON MERCH.STYLES TO ATTR_MGR;
GRANT SELECT ON MERCH.BARCODES TO ATTR_MGR;
GRANT SELECT ON MERCH.COLORS TO ATTR_MGR;
GRANT SELECT ON MERCH.SIZES TO ATTR_MGR;
GRANT SELECT ON MERCH.VENDORS TO ATTR_MGR;
GRANT SELECT ON MERCH.PRODUCT_TYPES TO ATTR_MGR;

-- Read/Write access to Shopify integration tables
GRANT SELECT, INSERT, UPDATE, DELETE ON MERCH.EXT_PRODUCTS TO ATTR_MGR;
GRANT SELECT, INSERT, UPDATE, DELETE ON MERCH.EXT_PRODUCT_VARIANTS TO ATTR_MGR;
GRANT SELECT ON MERCH.EXT_PRODUCTS_ACTIVITY TO ATTR_MGR;
GRANT SELECT ON MERCH.IRO_POS_EXT_PRODUCTS TO ATTR_MGR;

PROMPT --- STEP 2: GRANTS FROM OMNI TO ATTR_MGR ---

-- Read access to configuration
GRANT SELECT ON OMNI.BANNERS TO ATTR_MGR;
GRANT SELECT ON OMNI.PROVIDER_SERVICES TO ATTR_MGR;
GRANT SELECT ON OMNI.CLIENT_PARAMETERS TO ATTR_MGR;
GRANT SELECT ON OMNI.SITES TO ATTR_MGR;

-- Read access to order views
GRANT SELECT ON OMNI.V_ECOMM_ORDERS TO ATTR_MGR;
GRANT SELECT ON OMNI.V_ECOMM_ORDER_DETAILS TO ATTR_MGR;
GRANT SELECT ON OMNI.V_ECOMM_SHIPMENTS TO ATTR_MGR;
GRANT SELECT ON OMNI.V_ECOMM_CUSTOMERS TO ATTR_MGR;

-- Read access to staging tables (for monitoring)
GRANT SELECT ON OMNI.STG_ORDERS TO ATTR_MGR;
GRANT SELECT ON OMNI.STG_ORDER_DETAILS TO ATTR_MGR;

-- Execute on scheduler (if needed)
-- GRANT EXECUTE ON DBMS_SCHEDULER TO ATTR_MGR;

PROMPT --- STEP 3: GRANTS FROM VSTORE TO ATTR_MGR ---

GRANT SELECT ON VSTORE.MV_MERCHANDISE_HIERARCHY TO ATTR_MGR;
GRANT SELECT ON VSTORE.SKUINVENTORY TO ATTR_MGR;
-- GRANT EXECUTE ON VSTORE.INTFS_SHOPIFY_PK TO ATTR_MGR;  -- If direct calls needed

PROMPT --- STEP 4: CREATE SYNONYMS IN ATTR_MGR ---

-- Connect as ATTR_MGR or have CREATE SYNONYM privilege
-- These synonyms allow the backend to use simple table names

-- MERCH Synonyms
CREATE OR REPLACE SYNONYM ATTR_MGR.STYLES FOR MERCH.STYLES;
CREATE OR REPLACE SYNONYM ATTR_MGR.BARCODES FOR MERCH.BARCODES;
CREATE OR REPLACE SYNONYM ATTR_MGR.COLORS FOR MERCH.COLORS;
CREATE OR REPLACE SYNONYM ATTR_MGR.SIZES FOR MERCH.SIZES;
CREATE OR REPLACE SYNONYM ATTR_MGR.VENDORS FOR MERCH.VENDORS;
CREATE OR REPLACE SYNONYM ATTR_MGR.PRODUCT_TYPES FOR MERCH.PRODUCT_TYPES;
CREATE OR REPLACE SYNONYM ATTR_MGR.EXT_PRODUCTS FOR MERCH.EXT_PRODUCTS;
CREATE OR REPLACE SYNONYM ATTR_MGR.EXT_PRODUCT_VARIANTS FOR MERCH.EXT_PRODUCT_VARIANTS;
CREATE OR REPLACE SYNONYM ATTR_MGR.EXT_PRODUCTS_ACTIVITY FOR MERCH.EXT_PRODUCTS_ACTIVITY;
CREATE OR REPLACE SYNONYM ATTR_MGR.MERCH_EXT_PRODUCT_VARIANTS FOR MERCH.EXT_PRODUCT_VARIANTS;

-- OMNI Synonyms
CREATE OR REPLACE SYNONYM ATTR_MGR.BANNERS FOR OMNI.BANNERS;
CREATE OR REPLACE SYNONYM ATTR_MGR.PROVIDER_SERVICES FOR OMNI.PROVIDER_SERVICES;
CREATE OR REPLACE SYNONYM ATTR_MGR.CLIENT_PARAMETERS FOR OMNI.CLIENT_PARAMETERS;
CREATE OR REPLACE SYNONYM ATTR_MGR.SITES FOR OMNI.SITES;
CREATE OR REPLACE SYNONYM ATTR_MGR.V_ECOMM_ORDERS FOR OMNI.V_ECOMM_ORDERS;
CREATE OR REPLACE SYNONYM ATTR_MGR.V_ECOMM_ORDER_DETAILS FOR OMNI.V_ECOMM_ORDER_DETAILS;
CREATE OR REPLACE SYNONYM ATTR_MGR.V_ECOMM_SHIPMENTS FOR OMNI.V_ECOMM_SHIPMENTS;
CREATE OR REPLACE SYNONYM ATTR_MGR.V_ECOMM_CUSTOMERS FOR OMNI.V_ECOMM_CUSTOMERS;
CREATE OR REPLACE SYNONYM ATTR_MGR.STG_ORDERS FOR OMNI.STG_ORDERS;

-- VSTORE Synonyms
CREATE OR REPLACE SYNONYM ATTR_MGR.MV_MERCHANDISE_HIERARCHY FOR VSTORE.MV_MERCHANDISE_HIERARCHY;
CREATE OR REPLACE SYNONYM ATTR_MGR.SKUINVENTORY FOR VSTORE.SKUINVENTORY;

PROMPT
PROMPT ============================================================
PROMPT OPTION A COMPLETE: Grants and Synonyms created.
PROMPT ============================================================

